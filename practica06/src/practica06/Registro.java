    private void limpiar(){
        textFieldMarca.setText("");
        textFieldModelo.setText("");
        textFieldPrecio.setText("");
        textFieldResolucion.setText("");
        textFieldTamanoPantalla.setText("");
        checkAndroidTv.setSelected(false);
        checkRoku.setSelected(false);
        checkSaphi.setSelected(false);
        checkTizen.setSelected(false);
        checkWebOS.setSelected(false);
        labelMensaje.setText("");
        
        textFieldMarca.requestFocus();
    }
    
    private boolean estaVacio(){
      return textFieldMarca.getText().isEmpty() && textFieldModelo.getText().isEmpty() &&
                textFieldPrecio.getText().isEmpty() && textFieldResolucion.getText().isEmpty() 
                && !(checkAndroidTv.isSelected() | checkRoku.isSelected() | checkSaphi.isSelected()
                | checkWebOS.isSelected()) && textFieldTamanoPantalla.getText().isEmpty();
    }
    
    private void controlCheckBox(java.awt.event.ActionEvent evt){
        checkAndroidTv.setSelected(false);
        checkRoku.setSelected(false);
        checkSaphi.setSelected(false);
        checkTizen.setSelected(false);
        checkWebOS.setSelected(false);
        
        if(evt.getSource().equals(checkAndroidTv)) checkAndroidTv.setSelected(true);
        else if(evt.getSource().equals(checkRoku)) checkRoku.setSelected(true);
        else if(evt.getSource().equals(checkSaphi)) checkSaphi.setSelected(true);
        else if(evt.getSource().equals(checkTizen)) checkTizen.setSelected(true);
        else if(evt.getSource().equals(checkWebOS)) checkWebOS.setSelected(true);
    }
    
    private String obtenerValorCheckBox(){
        if(checkAndroidTv.isSelected()) return checkAndroidTv.getText();
        if(checkRoku.isSelected()) return checkRoku.getText();
        if(checkSaphi.isSelected()) return checkSaphi.getText();
        if(checkTizen.isSelected()) return checkTizen.getText();
        if(checkWebOS.isSelected()) return checkWebOS.getText();
        return "no seleccionado";
    }

