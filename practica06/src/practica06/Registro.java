    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        if(!estaVacio()){
            String marca = textFieldMarca.getText();
            String modelo = textFieldModelo.getText();
            float precio = Float.parseFloat(textFieldPrecio.getText());
            String resolucion = textFieldResolucion.getText();
            int tamanoPantalla = Integer.parseInt(textFieldTamanoPantalla.getText());
            String sistemaOperativo = obtenerValorCheckBox();
            
            smartv = new SmartTv(sistemaOperativo, resolucion, marca, modelo, tamanoPantalla, precio);
            
            limpiar();
        }else labelMensaje.setText("Ingrese todos los datos que se piden");
    }//GEN-LAST:event_btnAgregarActionPerformed

